function parseMarkdown(md) {
  const lines = md.trim().split('\\n');
  return lines.map((line, index) => {
    if (line.startsWith('### ')) {
      return <h3 key={index} className="policy-h3">{line.replace('### ', '')}</h3>;
    } else if (line.startsWith('# ')) {
      return <h2 key={index} className="policy-h2">{line.replace('# ', '')}</h2>;
    } else if (line.trim() === '---') {
      return <hr key={index} className="policy-hr" />;
    } else if (line.startsWith('- ')) {
      return <li key={index} className="policy-li" dangerouslySetInnerHTML={{ __html: formatInline(line.substring(2)) }} />;
    } else if (line.match(/^\\d+\\.\\s/)) {
      return <div key={index} className="policy-list-item" dangerouslySetInnerHTML={{ __html: formatInline(line) }} />;
    } else if (line.trim() === '') {
      return <br key={index} />;
    } else {
      return <p key={index} className="policy-p" dangerouslySetInnerHTML={{ __html: formatInline(line) }} />;
    }
  });
}

function formatInline(text) {
  return text.replace(/\\*\\*(.*?)\\*\\*/g, '<strong>$1</strong>');
}

